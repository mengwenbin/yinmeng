package com.xiaoxu.music.community.entity;

import java.io.Serializable;
import java.util.List;

public class AdvertisementEntity implements Serializable {
	
	private List<AdvertisementInfoEntity> list_res_head;
	private List<AdvertisementInfoEntity> list_res_boot;
	private List<AdvertisementInfoEntity> list_res_video;
	
	public AdvertisementEntity() {
		super();
	}
	
	public AdvertisementEntity(List<AdvertisementInfoEntity> list_res_head,
			List<AdvertisementInfoEntity> list_res_boot) {
		super();
		this.list_res_head = list_res_head;
		this.list_res_boot = list_res_boot;
	}

	public List<AdvertisementInfoEntity> getList_res_head() {
		return list_res_head;
	}

	public void setList_res_head(List<AdvertisementInfoEntity> list_res_head) {
		this.list_res_head = list_res_head;
	}

	public List<AdvertisementInfoEntity> getList_res_boot() {
		return list_res_boot;
	}

	public void setList_res_boot(List<AdvertisementInfoEntity> list_res_boot) {
		this.list_res_boot = list_res_boot;
	}

	public List<AdvertisementInfoEntity> getList_res_video() {
		return list_res_video;
	}

	public void setList_res_video(List<AdvertisementInfoEntity> list_res_video) {
		this.list_res_video = list_res_video;
	}

	@Override
	public String toString() {
		return "AdvertisementEntity [list_res_head=" + list_res_head
				+ ", list_res_boot=" + list_res_boot + ", list_res_video="
				+ list_res_video + "]";
	}
	
}
