package com.xiaoxu.music.community.entity;

import java.io.Serializable;

public class ApplyEntity implements Serializable {

	private String access_token;
	private String token;
	private String user_id;
	private StatuEntity User;

	public String getAccess_token() {
		return access_token;
	}

	public void setAccess_token(String access_token) {
		this.access_token = access_token;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public StatuEntity getUser() {
		return User;
	}

	public void setUser(StatuEntity user) {
		User = user;
	}

	public ApplyEntity() {
		super();
	}

	@Override
	public String toString() {
		return "ApplyEntity [access_token=" + access_token + ", token=" + token
				+ ", user_id=" + user_id + ", User=" + User + "]";
	}

}
