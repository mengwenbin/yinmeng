package com.xiaoxu.music.community.entity;

import java.io.Serializable;

public class CategorysInfoEntity implements Serializable {

	private String category_id;
	private String category_name;
	private String category_img;
	private String category_img2;
	private String category_summary;
	private String count_all;
	private String count_today;
	private String count_reply;
	
	public CategorysInfoEntity() {
		super();
	}
	
	public CategorysInfoEntity(String category_id) {
		super();
		this.category_id = category_id;
	}
	
	public CategorysInfoEntity(String category_id, String category_name,
			String category_img, String category_img2, String category_summary,
			String count_all, String count_today, String count_reply) {
		super();
		this.category_id = category_id;
		this.category_name = category_name;
		this.category_img = category_img;
		this.category_img2 = category_img2;
		this.category_summary = category_summary;
		this.count_all = count_all;
		this.count_today = count_today;
		this.count_reply = count_reply;
	}

	public String getCategory_id() {
		return category_id;
	}

	public void setCategory_id(String category_id) {
		this.category_id = category_id;
	}

	public String getCategory_name() {
		return category_name;
	}

	public void setCategory_name(String category_name) {
		this.category_name = category_name;
	}

	public String getCategory_img() {
		return category_img;
	}

	public void setCategory_img(String category_img) {
		this.category_img = category_img;
	}

	public String getCategory_img2() {
		return category_img2;
	}

	public void setCategory_img2(String category_img2) {
		this.category_img2 = category_img2;
	}

	public String getCategory_summary() {
		return category_summary;
	}

	public void setCategory_summary(String category_summary) {
		this.category_summary = category_summary;
	}

	public String getCount_all() {
		return count_all;
	}

	public void setCount_all(String count_all) {
		this.count_all = count_all;
	}

	public String getCount_today() {
		return count_today;
	}

	public void setCount_today(String count_today) {
		this.count_today = count_today;
	}

	public String getCount_reply() {
		return count_reply;
	}

	public void setCount_reply(String count_reply) {
		this.count_reply = count_reply;
	}
	
	@Override
	public String toString() {
		return "CategorysInfoEntity [category_id=" + category_id
				+ ", category_name=" + category_name + ", category_img="
				+ category_img + ", category_img2=" + category_img2
				+ ", category_summary=" + category_summary + ", count_all="
				+ count_all + ", count_today=" + count_today + ", count_reply="
				+ count_reply + "]";
	}
	
}
