package com.xiaoxu.music.community.entity;

import java.io.Serializable;
import java.util.List;

public class SongMenuDetailEntity implements Serializable {
	
	private SongMenuInfoEntity music_list;
	private String list_size;
	private List<SongInfoEntity> list_music;
	
	public SongMenuDetailEntity(SongMenuInfoEntity music_list,
			String list_size, List<SongInfoEntity> list_music) {
		super();
		this.music_list = music_list;
		this.list_size = list_size;
		this.list_music = list_music;
	}

	public SongMenuDetailEntity() {
		super();
	}
	
	public SongMenuInfoEntity getMusic_list() {
		return music_list;
	}
	public void setMusic_list(SongMenuInfoEntity music_list) {
		this.music_list = music_list;
	}
	public String getList_size() {
		return list_size;
	}
	public void setList_size(String list_size) {
		this.list_size = list_size;
	}
	public List<SongInfoEntity> getList_music() {
		return list_music;
	}
	public void setList_music(List<SongInfoEntity> list_music) {
		this.list_music = list_music;
	}
	@Override
	public String toString() {
		return "MusicListSongsEntity [music_list=" + music_list
				+ ", list_size=" + list_size + ", list_music=" + list_music
				+ "]";
	}
	
	
}
