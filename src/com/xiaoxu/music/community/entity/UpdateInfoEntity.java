package com.xiaoxu.music.community.entity;

import java.io.Serializable;

public class UpdateInfoEntity implements Serializable {

	private String path;
	private String version;
	private String updatelog;
	private String target_size;
	
	public String getUpdatelog() {
		return updatelog;
	}
	public void setUpdatelog(String updatelog) {
		this.updatelog = updatelog;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getTarget_size() {
		return target_size;
	}
	public void setTarget_size(String target_size) {
		this.target_size = target_size;
	}
	@Override
	public String toString() {
		return "UpdateInfoEntity [path=" + path + ", version=" + version
				+ ", updatelog=" + updatelog + ", target_size=" + target_size
				+ "]";
	}
	
}
