package com.xiaoxu.music.community.entity;

import java.io.Serializable;
import java.util.List;

public class VideoEntity implements Serializable {

	private String list_size;
	private List<AdvertisementInfoEntity> list_res;
	
	public String getList_size() {
		return list_size;
	}
	public void setList_size(String list_size) {
		this.list_size = list_size;
	}
	public List<AdvertisementInfoEntity> getList_res() {
		return list_res;
	}
	public void setList_res(List<AdvertisementInfoEntity> list_res) {
		this.list_res = list_res;
	}
	public VideoEntity() {
		super();
	}
	@Override
	public String toString() {
		return "VideoEntity [list_size=" + list_size + ", list_res=" + list_res
				+ "]";
	}

}
